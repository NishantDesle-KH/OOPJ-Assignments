 class GreatestScore {
    public static void main(String[] args) {
        int test1 = 78;
        int test2 = 85;

        if (test1 > test2) {
            System.out.println("Test 1 has higher score.");
        } else if (test2 > test1) {
            System.out.println("Test 2 has higher score.");
        } else {
            System.out.println("Both tests have equal scores.");
        }
    }
}
